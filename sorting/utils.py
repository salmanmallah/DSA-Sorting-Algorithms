def bubble_sort(arr):
    n = len(arr)
    # start_time = time.perf_counter()
    for i in range(n - 1):
        for j in range(0, n - i - 1):
            if arr[j] > arr[j + 1]:
                arr[j], arr[j + 1] = arr[j + 1], arr[j]
    return arr

